def new_mask():
    return Image.new("L", (S, S), 0)


def mosaic(mask, rgb, cell=26, grout=4, seed=1):
    """Fill the white area of `mask` with mirror tiles of color `rgb`."""
    rnd = random.Random(seed)
    tiles = Image.new("RGBA", (S, S), (0, 0, 0, 0))
    d = ImageDraw.Draw(tiles)
    h, s, v = colorsys.rgb_to_hsv(*[c / 255 for c in rgb])
    for y in range(0, S, cell):
        for x in range(0, S, cell):
            cx = min(x + cell // 2, S - 1)
            cy = min(y + cell // 2, S - 1)
            if mask.getpixel((cx, cy)) < 100:
                continue
            if rnd.random() < 0.07:  # occasional pure-mirror sparkle tile
                ss, vv = s * 0.10, 1.0
            else:
                vv = max(0.06, min(1.0, v * rnd.uniform(0.50, 1.30)))
                ss = min(1.0, s * rnd.uniform(0.85, 1.12))
            hh = (h + rnd.uniform(-0.012, 0.012)) % 1.0
            r, g, b = [int(255 * c) for c in colorsys.hsv_to_rgb(hh, ss, vv)]
            d.rectangle(
                [x + grout, y + grout, x + cell - 1, y + cell - 1],
                fill=(r, g, b, 255),
            )
    # dark grout base clipped to the glyph silhouette, tiles on top
    out = Image.new("RGBA", (S, S), (0, 0, 0, 0))
    grout_base = Image.new("RGBA", (S, S), (26, 27, 32, 255))
    out.paste(grout_base, (0, 0), mask)
    alpha = ImageChops.multiply(tiles.split()[3], mask)
    tiles.putalpha(alpha)
    out.alpha_composite(tiles)
    return out


def drop_shadow(mask, dy=12, blur=14, alpha=120):
    sh = Image.new("RGBA", (S, S), (0, 0, 0, 0))
    black = Image.new("RGBA", (S, S), (0, 0, 0, alpha))
    sh.paste(black, (0, dy), mask)
    return sh.filter(ImageFilter.GaussianBlur(blur))


def sparkle_stars(mask, n, seed):
    """A few 4-point light stars on top of the mosaic."""
    rnd = random.Random(seed * 7 + 3)
    img = Image.new("RGBA", (S, S), (0, 0, 0, 0))
    d = ImageDraw.Draw(img)
    pts, tries = [], 0
    while len(pts) < n and tries < 500:
        tries += 1
        x = rnd.randrange(70, S - 70)
        y = rnd.randrange(70, S - 70)
        if mask.getpixel((x, y)) > 100:
            if all((x - px) ** 2 + (y - py) ** 2 > 90 ** 2 for px, py in pts):
                pts.append((x, y))
    for x, y in pts:
        L = rnd.randrange(24, 42)
        d.line([x - L, y, x + L, y], fill=(255, 255, 255, 235), width=7)
        d.line([x, y - L, x, y + L], fill=(255, 255, 255, 235), width=7)
        h = L // 2
        d.line([x - h, y - h, x + h, y + h], fill=(255, 255, 255, 150), width=3)
        d.line([x - h, y + h, x + h, y - h], fill=(255, 255, 255, 150), width=3)
        d.ellipse([x - 9, y - 9, x + 9, y + 9], fill=(255, 255, 255, 255))
    return img.filter(ImageFilter.GaussianBlur(1))


def background(color, radius=112):
    img = Image.new("RGBA", (S, S), (0, 0, 0, 0))
    d = ImageDraw.Draw(img)
    d.rounded_rectangle([10, 10, S - 10, S - 10], radius=radius, fill=color + (255,))
    # soft gloss on the upper half
    gloss = new_mask()
    gd = ImageDraw.Draw(gloss)
    gd.rounded_rectangle([10, 10, S - 10, int(S * 0.46)], radius=radius, fill=34)
    white = Image.new("RGBA", (S, S), (255, 255, 255, 255))
    img.paste(white, (0, 0), gloss)
    # bright rim
    d.rounded_rectangle(
        [10, 10, S - 10, S - 10], radius=radius, outline=(255, 255, 255, 80), width=4
    )
    return img


def rot_rect(d, cx, cy, w, l, ang):
    a = math.radians(ang)
    ca, sa = math.cos(a), math.sin(a)
    pts = []
    for dx, dy in [(-w / 2, -l / 2), (w / 2, -l / 2), (w / 2, l / 2), (-w / 2, l / 2)]:
        pts.append((cx + dx * ca - dy * sa, cy + dx * sa + dy * ca))
    d.polygon(pts, fill=255)


