"""
Disco Icons generator.

Draws every icon of the pack from scratch: a flat rounded-square background
with the app's glyph rebuilt as a "discomorphism" mirror-ball mosaic —
a grid of small mirror tiles with random brightness, dark grout between
tiles, star sparkles and a soft drop shadow.

Runs on GitHub Actions before the APK is built:
    python3 tools/generate_icons.py <output-res-dir> <preview.png>

Every image here is original artwork generated by this code.
"""
